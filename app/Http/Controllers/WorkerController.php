    public function store(Request $request)
    {
        $worker = new Worker;
        $worker->name = $request->name;
        $worker->phone = $request->phone;
        $worker->location = $request->location;
        $worker->values = $request->values;
        $worker->description = $request->description;
        $worker->save();
        return back();
    }

